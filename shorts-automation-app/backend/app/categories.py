def get_category_info(category_code: str) -> dict:
    """카테고리 정보 조회"""
    return CHANNEL_CATEGORIES.get(category_code, {})


def get_category_prompt(category_code: str, num_ideas: int = 5) -> str:
    """카테고리별 프롬프트 템플릿 조회"""
    info = get_category_info(category_code)
    template = info.get("prompt_template", "")
    return template.format(num_ideas=num_ideas)
